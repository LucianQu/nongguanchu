public class UseWaterElec {
    private String date;
    private String waterCount;
    private String elecCount;
    private String waterEleRi;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getWaterCount() {
        return waterCount;
    }

    public void setWaterCount(String waterCount) {
        this.waterCount = waterCount;
    }

    public String getElecCount() {
        return elecCount;
    }

    public void setElecCount(String elecCount) {
        this.elecCount = elecCount;
    }

    public String getWaterEleRi() {
        return waterEleRi;
    }

    public void setWaterEleRi(String waterEleRi) {
        this.waterEleRi = waterEleRi;
    }

    public UseWaterElec() {
    }

    public UseWaterElec(String date, String waterCount, String elecCount, String waterEleRi) {
        this.date = date;
        this.waterCount = waterCount;
        this.elecCount = elecCount;
        this.waterEleRi = waterEleRi;
    }
}
