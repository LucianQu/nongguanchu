package com.automic.app.bean;

import java.io.Serializable;

/**
 * 水电不足报警
 * Created by qulus on 2017/4/13 0013.
 */

public class WaterEleclessWarn implements Serializable {
    private String wellNo ;     //机井编码
    private String wellName ;   //机井名称
    private String occurTime ;  //发生时间
    private String remainWater ;//剩余水量
    private String remainElect ;//剩余电量

    public String getWellNo() {
        return wellNo;
    }

    public void setWellNo(String wellNo) {
        this.wellNo = wellNo;
    }

    public String getWellName() {
        return wellName;
    }

    public void setWellName(String wellName) {
        this.wellName = wellName;
    }

    public String getOccurTime() {
        return occurTime;
    }

    public void setOccurTime(String occurTime) {
        this.occurTime = occurTime;
    }

    public String getRemainWater() {
        return remainWater;
    }

    public void setRemainWater(String remainWater) {
        this.remainWater = remainWater;
    }

    public String getRemainElect() {
        return remainElect;
    }

    public void setRemainElect(String remainElect) {
        this.remainElect = remainElect;
    }

    public WaterEleclessWarn(String wellNo, String wellName, String occurTime, String remainWater, String remainElect) {
        this.wellNo = wellNo ;
        this.wellName = wellName ;
        this.occurTime = occurTime ;
        this.remainWater = remainWater ;
        this.remainElect = remainElect ;
    }
}
