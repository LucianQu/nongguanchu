public class RechargeRecoder {
    private String wellNo;        //	机井编码
    private String wellName;        //	机井名称
    private String RechargeTime;        //	充值时间，精确到分
    private String buyWaterCount;        //	本次购水量
    private String remainWater;        //	剩余水量
    private String buyElectCount;        //	本次购电量
    private String remainElect;        //	剩余电量

    public String getWellNo() {
        return wellNo;
    }

    public void setWellNo(String wellNo) {
        this.wellNo = wellNo;
    }

    public String getWellName() {
        return wellName;
    }

    public void setWellName(String wellName) {
        this.wellName = wellName;
    }

    public String getRechargeTime() {
        return RechargeTime;
    }

    public void setRechargeTime(String rechargeTime) {
        RechargeTime = rechargeTime;
    }

    public String getBuyWaterCount() {
        return buyWaterCount;
    }

    public void setBuyWaterCount(String buyWaterCount) {
        this.buyWaterCount = buyWaterCount;
    }

    public String getRemainWater() {
        return remainWater;
    }

    public void setRemainWater(String remainWater) {
        this.remainWater = remainWater;
    }

    public String getBuyElectCount() {
        return buyElectCount;
    }

    public void setBuyElectCount(String buyElectCount) {
        this.buyElectCount = buyElectCount;
    }

    public String getRemainElect() {
        return remainElect;
    }

    public void setRemainElect(String remainElect) {
        this.remainElect = remainElect;
    }

    public RechargeRecoder(String wellNo, String wellName, String rechargeTime, String buyWaterCount, String remainWater, String buyElectCount, String remainElect) {
        this.wellNo = wellNo;
        this.wellName = wellName;
        RechargeTime = rechargeTime;
        this.buyWaterCount = buyWaterCount;
        this.remainWater = remainWater;
        this.buyElectCount = buyElectCount;
        this.remainElect = remainElect;
    }
}
